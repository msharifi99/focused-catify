function bgRandom() {
  var x = "";
  $(".lcs_check").prop("checked", false);

  if (localStorage["tab"] == "" || localStorage["tab"] == undefined) {
    localStorage["tab"] = 1;
  } else if (localStorage["tab"] != "" && localStorage["tab"] != undefined) {
    localStorage["tab"] = ++localStorage["tab"];
  }

  localStorage["tab"] = localStorage["tab"] % 159;

  if (localStorage["tab"] == 0) {
    localStorage["tab"] = 1;
  }
  //if(localStorage["tab"] == 163) {
  //localStorage["tab"] = 1;
  //}
  x = localStorage["tab"];
  //else if(isNaN(x)) {
  //localStorage["tab"] = 1;
  //}
  //alert(localStorage["tab"]);
  var catProps = CATIFY_CAT_BY_TAB_NUM[x] || {};
  $("body").css("background-image", `url("images/${catProps.imgname}.gif")`);
  $("body").css("background-color", catProps.colormain);
  $(".setting-icon").css("color", catProps.colorsecond);
  $(".weblink").css("color", catProps.colormain);
  $(".weblink").css("background", catProps.colorsecond);
}

$(document).ready(function () {
  $(".topsites").hide();
  bgRandom();
  $("#pgh-show").hide();
});
